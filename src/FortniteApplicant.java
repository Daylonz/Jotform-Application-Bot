public class FortniteApplicant {
    private String discordName;
    private String streamName;
    private String age;
    private String wins;
    private String winPercentage;
    private String killDeath;
    private String origin;
    private String server;
    private String epicName;
    private String fortniteTracker;
    private String strongestAsset;
    private String weakestAsset;
    private String scrimmageExperience;
    private String experience;
    private String goals;
    private String anythingElse;


    public FortniteApplicant() {
    }

    public FortniteApplicant(String discordName, String steamName, String age) {
        this.discordName = discordName;
        this.streamName = steamName;
        this.age = age;
    }

    public String getServer() {
        return server;
    }

    public void setServer(String server) {
        this.server = server;
    }

    public String getEpicName() {
        return epicName;
    }

    public void setEpicName(String epicName) {
        this.epicName = epicName;
    }

    public String getFortniteTracker() {
        return fortniteTracker;
    }

    public void setFortniteTracker(String fortniteTracker) {
        this.fortniteTracker = fortniteTracker;
    }

    public String getStrongestAsset() {
        return strongestAsset;
    }

    public void setStrongestAsset(String strongestAsset) {
        this.strongestAsset = strongestAsset;
    }

    public String getWeakestAsset() {
        return weakestAsset;
    }

    public void setWeakestAsset(String weakestAsset) {
        this.weakestAsset = weakestAsset;
    }

    public String getScrimmageExperience() {
        return scrimmageExperience;
    }

    public void setScrimmageExperience(String scrimmageExperience) {
        this.scrimmageExperience = scrimmageExperience;
    }

    public String getKillDeath() {
        return killDeath;
    }

    public void setKillDeath(String killDeath) {
        this.killDeath = killDeath;
    }

    public String getWins() {
        return wins;
    }

    public void setWins(String wins) {
        this.wins = wins;
    }

    public String getWinPercentage() {
        return winPercentage;
    }

    public void setWinPercentage(String winPercentage) {
        this.winPercentage = winPercentage;
    }

    public String getDiscordName() {
        return discordName;
    }

    public String getStreamName() {
        return streamName;
    }

    public String getAge() {
        return age;
    }

    public void setDiscordName(String discordName) {
        this.discordName = discordName;
    }

    public void setStreamName(String streamName) {
        this.streamName = streamName;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getAnythingElse() {
        return anythingElse;
    }

    public void setAnythingElse(String anythingElse) {
        this.anythingElse = anythingElse;
    }

    public String getOrigin() {
        return origin;
    }

    public void setOrigin(String origin) {
        this.origin = origin;
    }

    public String getExperience() {
        return experience;
    }

    public void setExperience(String experience) {
        this.experience = experience;
    }

    public String getGoals() {
        return goals;
    }

    public void setGoals(String goals) {
        this.goals = goals;
    }
}
